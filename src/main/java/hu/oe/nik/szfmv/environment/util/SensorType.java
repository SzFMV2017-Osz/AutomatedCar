public enum SensorType {
    CAMERA, RADAR, ULTRASONIC_FR, ULTRASONIC_FL, ULTRASONIC_RL, ULTRASONIC_RR,
    ULTRASONIC_LSF, ULTRASONIC_LSB, ULTRASONIC_RSF, ULTRASONIC_RSB


    /*
    Ultrasound sensors:
    FL  : front left
    FR  : front right
    LSF : left side front
    LSB : left side back
    RSF : right side front
    RSB : right side back
    RL  : rear left
    RR  : rear right

        FL  FR

     LSF       RSF


     LSB       RSB

        RL  RR

     */
}
